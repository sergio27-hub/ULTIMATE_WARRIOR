    private void PintarImagen(JLabel logo1, String ruta){
        this.imagen = new ImageIcon(ruta);
        this.icono = new ImageIcon(this.imagen.getImage().getScaledInstance(
                logo1.getWidth(),
                logo1.getHeight() ,
                Image.SCALE_DEFAULT )
        );
        logo1.setIcon(this.icono);
        this.repaint();
    }
    

